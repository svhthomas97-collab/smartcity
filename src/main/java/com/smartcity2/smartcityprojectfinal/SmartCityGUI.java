    public SmartCityGUI() {
        initComponents();
        
        new Thread(() -> {
            try {
                // ServiceDiscovery using jmdns
                ServiceDiscovery sd1 = new ServiceDiscovery("_grpc._tcp.local.", "NeighborhoodService");
                int Np = sd1.discoverService(5000);

                ServiceDiscovery sd2 = new ServiceDiscovery("_grpc._tcp.local.", "AirQualityService");
                int Ap = sd2.discoverService(5000);

                ServiceDiscovery sd3 = new ServiceDiscovery("_grpc._tcp.local.", "BudgetService");
                int Bp = sd3.discoverService(5000);

                // IF JMDNS doesnt find use fixed ports as fallback
                if (Np == -1) {
                    Np = 50051;
                }
                if (Ap == -1) {
                    Ap = 50052;
                }
                if (Bp == -1) {
                    Bp = 50053;
                }
                
                final int neighborhoodPort = Np;
                final int airPort = Ap;
                final int budgetPort = Bp;

                ManagedChannel channel = ManagedChannelBuilder
                        .forAddress("localhost", neighborhoodPort)
                        .usePlaintext()
                        .build();

                ManagedChannel channel2 = ManagedChannelBuilder
                        .forAddress("localhost", airPort)
                        .usePlaintext()
                        .build();

                ManagedChannel channel3 = ManagedChannelBuilder
                        .forAddress("localhost", budgetPort)
                        .usePlaintext()
                        .build();

                blockingStub = NeighborhoodServiceGrpc.newBlockingStub(channel);
                asyncStub = NeighborhoodServiceGrpc.newStub(channel);
                airAsyncStub = AirQualityServiceGrpc.newStub(channel2);
                budgetAsyncStub = BudgetAllocationServiceGrpc.newStub(channel3);

            } catch (Exception e) {
                e.printStackTrace();
            }
        }).start();
    }

