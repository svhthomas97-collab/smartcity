    private void StartMonitoringActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_StartMonitoringActionPerformed

        String zone = ZoneID.getText().trim();

        if (zone.isEmpty()) {
            result2Area.setText("Enter zone\n");
            return;
        }

        result2Area.setText("Monitoring started...\n");

        Zone request = Zone.newBuilder()
                .setZone(zone)
                .build();

        airAsyncStub.monitorAirQuality(request,
                new StreamObserver<AirQuality>() {

            @Override
            public void onNext(AirQuality value) {
                result2Area.append(
                        "Zone: " + value.getZone()
                        + " | PM2.5: " + value.getPm25()
                        + " | CO2: " + value.getCo2()
                        + " | NO2: " + value.getNo2()
                        + " | Level: " + value.getLevel() + "\n"
                );
            }

            @Override
            public void onError(Throwable t) {
                String msg = t.getMessage();

                if (msg != null && msg.contains("Zone can't be empty")) {
                    result2Area.append("Invalid zone. Please enter a valid one.\n");
                } else {
                    result2Area.append("gRPC Error: " + msg + "\n");
                }
            }

            @Override
            public void onCompleted() {
                result2Area.append("Monitoring finished\n");
            }
        });

    }//GEN-LAST:event_StartMonitoringActionPerformed

