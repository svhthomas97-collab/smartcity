    private void startButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_startButtonActionPerformed
        requestObserver = asyncStub.analyzeNeighborhoods(
                new StreamObserver<NeighborhoodList>() {

            @Override
            public void onNext(NeighborhoodList value) {
                resultArea.append("Final result:\n");
                value.getNeighborhoodsList().forEach(n
                        -> resultArea.append(n.toString() + "\n"));
            }

            @Override
            public void onError(Throwable t) {
                String msg = t.getMessage();

                if (msg != null && msg.contains("not found")) {
                    resultArea.append("Invalid neighborhood ID\n");
                } else {
                    resultArea.append("gRPC Error: " + msg + "\n");
                }
            }

            @Override
            public void onCompleted() {
                resultArea.append("Stream completed\n");
            }
        });

        resultArea.setText("Stream started...\n"); // TODO add your handling code here:
    }//GEN-LAST:event_startButtonActionPerformed

