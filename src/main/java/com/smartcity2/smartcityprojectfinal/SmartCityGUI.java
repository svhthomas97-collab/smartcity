    private void StartBudgetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_StartBudgetActionPerformed
        budgetRequestObserver = budgetAsyncStub.allocateBudget(
                new StreamObserver<BudgetPlan>() {

            @Override
            public void onNext(BudgetPlan value) {
                Result3Area.append(
                        "Neighborhood: " + value.getNeighborhoodId()
                        + " | Allocated: " + value.getAllocatedAmount()
                        + " | Status: " + value.getStatus() + "\n"
                );
            }

            @Override
            public void onError(Throwable t) {
                String msg = t.getMessage();

                if (msg != null && msg.contains("not found")) {
                    Result3Area.append("Neighborhood not found\n");
                } else {
                    Result3Area.append("gRPC Error: " + msg + "\n");
                }
            }

            @Override
            public void onCompleted() {
                Result3Area.append("Budgetting completed\n");
            }
        });

        Result3Area.setText("Budget stream started\n");
    }//GEN-LAST:event_StartBudgetActionPerformed

    private void SendBudgetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SendBudgetActionPerformed
        if (budgetRequestObserver == null) {
            Result3Area.setText("Click START first\n");
            return;
        }

        String id = InsertID.getText().trim();
        int priority = Integer.parseInt(PriorityField.getText().trim());

        BudgetPriorityRequest request = BudgetPriorityRequest.newBuilder()
                .setNeighborhoodId(id)
                .setPriorityLevel(priority)
                .build();

        budgetRequestObserver.onNext(request);

        Result3Area.append("Sent: " + id + " (priority " + priority + ")\n");
    }//GEN-LAST:event_SendBudgetActionPerformed

